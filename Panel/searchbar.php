<script>
    document.addEventListener("DOMContentLoaded", function() {
        // Load saved theme settings on page load
        loadThemeSettings();
    });

    function saveTheme() {
        // Collect selected values for theme styles, header colors, and sidebar colors
        var selectedThemeStyle = document.querySelector('input[name="flexRadioDefault"]:checked').id;
        var selectedHeaderColor = getSelectedColor('headercolor');
        var selectedSidebarColor = getSelectedColor('sidebarcolor');

        // Save the selected theme settings
        localStorage.setItem('themeStyle', selectedThemeStyle);
        localStorage.setItem('headerColor', selectedHeaderColor);
        localStorage.setItem('sidebarColor', selectedSidebarColor);

        // Apply the selected theme settings (replace this with your actual implementation)
        applyTheme(selectedThemeStyle, selectedHeaderColor, selectedSidebarColor);

        console.log("Theme settings saved!");
        alert("Theme settings saved!");
    }

    function loadThemeSettings() {
        // Load and apply saved theme settings
        var savedThemeStyle = localStorage.getItem('themeStyle') || 'semidark';
        var savedHeaderColor = localStorage.getItem('headerColor') || 'headercolor1';
        var savedSidebarColor = localStorage.getItem('sidebarColor') || 'sidebarcolor1';

        applyTheme(savedThemeStyle, savedHeaderColor, savedSidebarColor);

        console.log("Theme settings loaded!");
    }

    function getSelectedColor(colorType) {
        // Get the selected color for header or sidebar
        var colorInputs = document.querySelectorAll('input[name="' + colorType + '"]:checked');
        return colorInputs.length > 0 ? colorInputs[0].id : null;
    }

    function applyTheme(themeStyle, headerColor, sidebarColor) {
        // Apply the selected theme settings (replace this with your actual implementation)
        document.body.className = ''; // Clear existing classes
        document.body.classList.add(themeStyle, headerColor, sidebarColor);
    }

    function setDefaultTheme() {
        // Reset radio buttons to default
        document.getElementById('semidark').checked = true;
        document.getElementById('headercolor1').checked = true;
        document.getElementById('sidebarcolor1').checked = true;

        // Apply default theme settings (replace this with your actual implementation)
        applyTheme('semidark', 'headercolor1', 'sidebarcolor1');

        // Clear saved theme settings from localStorage
        localStorage.removeItem('themeStyle');
        localStorage.removeItem('headerColor');
        localStorage.removeItem('sidebarColor');

        console.log("Theme set to default!");
        alert("Theme set to default!");
    }
</script>
