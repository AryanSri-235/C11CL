document.addEventListener('DOMContentLoaded', function () {
  var stepper1 = new Stepper(document.querySelector('#stepperr'));
  var currentStep = 0; // Track current step

  var form = document.querySelector('form');
  var formSteps = form.querySelectorAll('.bs-stepper-pane');

  form.addEventListener('submit', function (event) {
    event.preventDefault(); // Prevent default form submission

    var currentFormField = form.querySelector(`#test-l-${currentStep + 1} input`);

    if (currentFormField.checkValidity()) {
      // Move to the next step if validations pass
      if (currentStep < formSteps.length - 1) {
        stepper1.next();
        currentStep++;
      } else {
        // Last step reached, perform form submission
        // Add a hidden input field to mark the addground action
        var addgroundInput = document.createElement('input');
        addgroundInput.setAttribute('type', 'hidden');
        addgroundInput.setAttribute('name', 'addground');
        addgroundInput.setAttribute('value', '1');
        form.appendChild(addgroundInput);

		var addtrialvenueInput = document.createElement('input');
        addtrialvenueInput.setAttribute('type', 'hidden');
        addtrialvenueInput.setAttribute('name', 'addtrialvenue');
        addtrialvenueInput.setAttribute('value', '1');
        form.appendChild(addtrialvenueInput);

		var addstatematchvenueInput = document.createElement('input');
        addstatematchInput.setAttribute('type', 'hidden');
        addstatematchInput.setAttribute('name', 'addstatematch');
        addstatematchInput.setAttribute('value', '1');
        form.appendChild(addstatematchInput);
        
        form.submit(); // Submit the form
      }
	  
    } else {
      currentFormField.reportValidity();
    }
  });

  // Add event listener for "Previous" buttons in each step
  var previousButtons = document.querySelectorAll('.btn-outline-secondary');
  previousButtons.forEach(function (button, index) {
    button.addEventListener('click', function () {
      if (currentStep > 0) {
        stepper1.previous();
        currentStep--;
      }
    });
  });
});
