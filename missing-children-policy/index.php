<div class="c11p-content">
    <p class="c11p-intro">At Champions 11 Cricket League, the safety and well-being of all children involved in our cricket league is our top priority. This policy outlines the steps we will take if a child goes missing during any event, match, or league activity.</p>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-running"></i></span><h3>1. Immediate Action</h3></div>
        <p>If a child is found missing:</p>
        <ul>
            <li>The area will be searched immediately by staff, volunteers, and responsible adults.</li>
            <li>One person will inform the ground security and local authorities if the child is not found within 10 minutes.</li>
            <li>Other children will be kept safe and supervised.</li>
        </ul>
    </div>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-phone-alt"></i></span><h3>2. Communication</h3></div>
        <ul>
            <li>Parents or guardians will be contacted right away.</li>
            <li>A clear description of the child will be shared with all staff and volunteers.</li>
            <li>The incident will be reported to the police if the child is not found quickly.</li>
        </ul>
    </div>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-search"></i></span><h3>3. Search Procedure</h3></div>
        <ul>
            <li>Nearby areas such as restrooms, tents, stands, and parking zones will be searched first.</li>
            <li>Entry and exit points will be monitored.</li>
            <li>All staff will stop their regular duties and assist in locating the child.</li>
        </ul>
    </div>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-smile"></i></span><h3>4. When the Child is Found</h3></div>
        <ul>
            <li>The child's safety will be confirmed.</li>
            <li>Parents or guardians will be immediately informed.</li>
            <li>The incident will be reviewed to prevent future issues.</li>
            <li>Emotional support will be given to the child if needed.</li>
        </ul>
    </div>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-shield-alt"></i></span><h3>5. Prevention Measures</h3></div>
        <ul>
            <li>Children must always be accompanied by a parent/guardian or assigned volunteer.</li>
            <li>Entry passes or ID tags will be provided to children during the event.</li>
            <li>Regular headcounts will be conducted by team managers or volunteers.</li>
            <li>Staff and volunteers will be trained on how to handle such situations.</li>
        </ul>
    </div>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-file-alt"></i></span><h3>6. Reporting and Documentation</h3></div>
        <ul>
            <li>A written report will be prepared and kept on record.</li>
            <li>The incident will be discussed in the next committee meeting for better planning.</li>
        </ul>
    </div>
    <div class="c11p-quote">"We are committed to creating a safe and secure environment for all children. Every child matters."</div>
