    document.addEventListener("DOMContentLoaded", function () {
        // Isolated Coverflow Core Instance Engine Mapping
        var uniqueC11ReelSwiper = new Swiper(".myUniqueReelSwiper", {
            effect: "coverflow", 
            grabCursor: true,
            centeredSlides: true,
            slidesPerView: "auto",
            loop: true,
            coverflowEffect: {
                rotate: 15,       
                stretch: 0,       
                depth: 150,       
                modifier: 1,      
                slideShadows: false 
            },
            loopAdditionalSlides: 3,
            watchSlidesProgress: true,
            touchStartPreventDefault: false,
            pagination: {
                el: ".c11r-swiper-container .swiper-pagination",
                clickable: true,
            },
            // Connected arrow elements to swiper engine mapping layers
            navigation: {
                nextEl: ".c11r-arrow-next",
                prevEl: ".c11r-arrow-prev",
            },
            on: {
                init: function () {
                    manageUniqueC11ActiveVideo(this);
                },
                slideChangeTransitionEnd: function () {
                    manageUniqueC11ActiveVideo(this);
                }
            }
        });

        // Unique dynamic control handler layers
        function manageUniqueC11ActiveVideo(swiperInstance) {
            var totalVideosInSection = swiperInstance.el.querySelectorAll('video');
            totalVideosInSection.forEach(function (vid) {
                vid.pause();
            });

            var dynamicActiveSlide = swiperInstance.slides[swiperInstance.activeIndex];
            if (dynamicActiveSlide) {
                var centerTargetVid = dynamicActiveSlide.querySelector('video');
                if (centerTargetVid) {
                    centerTargetVid.muted = true;
                    var dynamicPromise = centerTargetVid.play();
                    if (dynamicPromise !== undefined) {
                        dynamicPromise.catch(function () {});
                    }
                }
            }
        }

        // Event delegation using uniquely modified target classes
        document.querySelector('.myUniqueReelSwiper').addEventListener('click', function (event) {
            var targetedSlideCard = event.target.closest('.c11r-swiper-slide-card');
            if (targetedSlideCard) {
                var domVideoNode = targetedSlideCard.querySelector('video');
                if (domVideoNode) {
                    var sourceVideoPath = domVideoNode.getAttribute('src');
                    openC11UniqueReelModalWindow(sourceVideoPath);
                }
            }
        });
    });

    function openC11UniqueReelModalWindow(videoSourcePath) {
        var domModalNode = document.getElementById('c11UniqueReelModal');
        var domModalVideoNode = document.getElementById('c11UniqueModalVideo');
        
        domModalVideoNode.src = videoSourcePath;
        domModalVideoNode.muted = false; 
        domModalNode.style.display = 'flex';
        domModalVideoNode.play();
    }

    function closeC11UniqueReelModalWindow() {
        var domModalNode = document.getElementById('c11UniqueReelModal');
        var domModalVideoNode = document.getElementById('c11UniqueModalVideo');
        
        domModalNode.style.display = 'none';
        domModalVideoNode.pause();
        domModalVideoNode.src = ""; 
    }

    function closeC11UniqueReelModalOutside(eventInstance) {
        var domOverlayFrame = document.getElementById('c11UniqueReelModal');
        if (eventInstance.target === domOverlayFrame) {
            closeC11UniqueReelModalWindow();
        }
    }
