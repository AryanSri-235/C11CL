    <p class="c11p-intro">At Champions 11 Cricket League (C11CL), we believe in fair play, sportsmanship, and integrity both on and off the field. To ensure our cricketing events are conducted in a transparent and ethical manner, this Prevention of Malpractices Code has been created.</p>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-bullseye"></i></span><h3>1. Purpose of This Code</h3></div>
        <p>The purpose of this code is to:</p>
        <ul>
            <li>Promote honesty and discipline in the game.</li>
            <li>Prevent any form of cheating or wrongdoing.</li>
            <li>Protect the true spirit of cricket.</li>
            <li>Ensure equal opportunity for all players.</li>
        </ul>
    </div>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-times-circle"></i></span><h3>2. Malpractices Strictly Prohibited</h3></div>
        <p>The following actions are considered malpractices and are not allowed under any circumstances:</p>
        <ul>
            <li><strong>Match Fixing:</strong> Influencing the result of a match for personal or financial gain.</li>
            <li><strong>Spot Fixing:</strong> Manipulating a specific part of the match for unfair reasons.</li>
            <li><strong>Bribery or Corruption:</strong> Offering money, gifts, or benefits to players, umpires, or staff.</li>
            <li><strong>Playing Under a Fake Name or Identity.</strong></li>
            <li><strong>Age Fraud:</strong> Submitting false age proofs or documents.</li>
            <li><strong>Ball or Equipment Tampering</strong> in an illegal way.</li>
            <li><strong>Unethical Social Media Behavior:</strong> Spreading rumors or defaming others.</li>
            <li><strong>Disrespecting Umpires or Officials:</strong> Arguing, shouting, or using foul language.</li>
            <li><strong>Doping or Use of Banned Substances</strong> (refer to Anti-Doping Code).</li>
        </ul>
    </div>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-flag"></i></span><h3>3. Reporting Malpractices</h3></div>
        <p>If you see or experience any wrongdoing, report it immediately to the league organizers through:</p>
        <p>📧 <strong>Email:</strong> <a href="mailto:info@c11cl.com" style="color:#dc2618;">info@c11cl.com</a></p>
        <p>All reports will be kept confidential and will be seriously investigated. Whistleblowers will be protected.</p>
    </div>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-gavel"></i></span><h3>4. Consequences of Violation</h3></div>
        <p>If any individual or team is found guilty of breaking this code:</p>
        <ul>
            <li>Immediate disqualification from the tournament or league.</li>
            <li>Ban from future events of C11CL.</li>
            <li>Confiscation of awards, certificates, or trophies.</li>
            <li>Public announcement of the misconduct (if required).</li>
            <li>Legal action, if the malpractice involves fraud, forgery, or criminal behavior.</li>
        </ul>
    </div>
    <div class="c11p-section">
        <div class="c11p-section-heading"><span class="c11p-icon"><i class="fas fa-handshake"></i></span><h3>5. Responsibilities of Players and Teams</h3></div>
        <ul>
            <li>Always play fairly and by the rules.</li>
            <li>Maintain discipline and sportsmanship.</li>
            <li>Avoid shortcuts to win.</li>
            <li>Respect your opponents, umpires, and all event organizers.</li>
            <li>Keep the spirit of the game alive.</li>
        </ul>
    </div>
